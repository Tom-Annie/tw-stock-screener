st.markdown("## 7. 美股連動（建議權重 12%）🆕")

col1, col2 = st.columns([2, 1])
with col1:
    st.markdown("""
**原理：** 台股受美股影響極大（尤其電子股），費半指數和台積電 ADR 是隔日走勢的領先指標。夜盤則是台灣投資人對美股走勢的即時反應。

**三個子指標：**

**費半指數趨勢**（最高 35 分）
- 前一日漲 > 1% → 10 分，漲 > 0% → 5 分
- 5 日漲幅 > 3% → 15 分，> 1% → 10 分
- 站上 10 日均線 → 10 分

**台積電 ADR 折溢價**（最高 35 分）
- ADR 換算台幣價與台股價格比較
- 溢價 > 2% → 25 分（國際投資人看好）
- 溢價 1~2% → 20 分
- 溢價 0.3~1% → 15 分
- ADR 自身 5 日漲幅也納入計分

**夜盤價差**（最高 30 分）
- 夜盤收盤 vs 日盤收盤的漲跌幅
- 上漲 > 1% → 20 分
- 上漲 0.5~1% → 15 分
- 連續 3 天夜盤上漲 → 額外 10 分
""")

with col2:
    st.markdown("#### 訊號對照表")
    st.markdown("""
| 訊號 | 意思 |
|------|------|
| `費半+1.5%` | 費半昨天漲 1.5% |
| `費半-2.3%` | 費半昨天跌 2.3% |
| `ADR溢價1.2%` | 台積電ADR比台股貴 1.2% |
| `ADR折價0.8%` | 台積電ADR比台股便宜 |
| `夜盤+0.65%` | 夜盤比日盤收高 0.65% |
| `夜盤-0.30%` | 夜盤比日盤收低 |
""")

with st.expander("實戰用法", expanded=False):
    st.markdown("""
- **費半大漲 + ADR 溢價 + 夜盤上漲** → 隔天台股開高機率極大，電子股可偏多
- **費半大跌 + ADR 折價 + 夜盤下跌** → 隔天台股低開，避免追高
- 此策略的分數 **所有股票相同**（反映整體市場氛圍），搭配其他策略判斷個股強弱
- **電子股**受美股影響大，傳產/金融影響較小，可考慮對電子股加大此策略權重

**ADR 折溢價怎麼算：**
- 1 張台積電 ADR = 5 股台積電台股
- ADR 換算台幣價 = ADR 美元價 × 匯率(≈32) ÷ 5
- 溢價 = (ADR換算價 - 台股價) ÷ 台股價 × 100%
""")

